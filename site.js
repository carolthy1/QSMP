function updatePizza() {
  var pizza = document.getElementById("pizza");

  var checkboxes = document.querySelectorAll('input[type="checkbox"]');
  var button = document.getElementById("finalbutton29");

  var isChecked = false;

  for (var i = 0; i < checkboxes.length; i++) {
    var checkbox = checkboxes[i];
    var ingredientId = checkbox.id.replace("checkbox-", "");
    var ingredientImage = document.getElementById(ingredientId);

    if (checkbox.checked) {
      isChecked = true;
      ingredientImage.style.display = "block";
    } else {
      ingredientImage.style.display = "none";
    }
  }

  if (isChecked) {
    button.style.display = "block";
  } else {
    button.style.display = "none";
  }
}

